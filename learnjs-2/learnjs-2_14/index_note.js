// конструкция switch - case

// пример кода

let a = 2 + 2
switch (a) {
    case 3:
        console.log('маловато');
        break;

    case 4:
        console.log('в точку');
        break;

    case 3:
        console.log('перебор');
        break;

    default:
        console.log('нет таких значений');
}